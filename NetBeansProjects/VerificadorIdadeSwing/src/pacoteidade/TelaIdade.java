        int idade = 2023 - ano;
        
        lblIdade.setText(Integer.toString(idade));
        
        String sit = ((idade >= 16 && idade < 18) || (idade > 70)) ? "VOTO OPCIONAL" : "VOTO OBRIGATÓRIO";
        
        lblSituacao.setText(sit);
